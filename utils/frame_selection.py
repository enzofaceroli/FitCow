import os
import shutil 

def select_frames(input_dict):
    frames_path = "assets/Frames"
    output_path = os.path.join(frames_path, "selecionados")
    
    os.makedirs(output_path, exist_ok=True)

    
    for video, frames in input_dict.items():
        input_path = os.path.join(frames_path, video)        
        
        for frame in frames:
            filename = f"{video}_f{frame:05d}.jpg" 
            file = os.path.join(input_path, filename)
            
            if os.path.exists(file):
                destination_file = os.path.join(output_path, filename)
                
                if not os.path.exists(destination_file):
                    shutil.copy2(file, destination_file)
                    print(f"Copied: {filename}")
            
            else:
                print(f"File not found: {file}")

